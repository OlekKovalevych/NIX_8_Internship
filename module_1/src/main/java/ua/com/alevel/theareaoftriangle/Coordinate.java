package ua.com.alevel.theareaoftriangle;

public class Coordinate {

    private int x;
    private int y;

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public int getX() {
        return this.x;
    }
}
